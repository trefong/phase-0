def get_grade (x)

  if x >= 90 && x<= 100
  then puts "A"

  elsif x>=80 && x<=89
  then puts "B"

  elsif x>=70 && x<=79
  then puts "C"

  elsif x>=60 && x<=69
  then puts "D"

  else puts "F"

  end
end




